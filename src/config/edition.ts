// Издание сборки по доступности данных 1С. Значение задаётся per-копия:
//   full  — 1С подключён: полная аналитика (Копия 1)
//   gated — 1С НЕ подключён: блоки 1С-аналитики закрыты карточкой-приглашением
//           «Аналитика 1С» (Копия 2). ЭЦП-функции (ЭСФ/Налоги/Отчёты) работают.
//   off   — редакция без 1С: блоки 1С-аналитики отсутствуют (Копия 3)
export type OnecMode = "full" | "gated" | "off";

// `as OnecMode` — чтобы TS не сужал до литерала и сравнения ниже были валидны.
export const ONEC_MODE = "off" as OnecMode;

/** Есть доступ к данным 1С (полная аналитика). */
export const HAS_ONEC = ONEC_MODE === "full";
/** 1С не подключён, но блоки показываем как «закрытые» с приглашением. */
export const ONEC_GATED = ONEC_MODE === "gated";
